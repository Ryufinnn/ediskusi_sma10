Visi dan Misi</h1>
<p>
<b>Visi :</b><br>
Terwujudnya Insan Cerdas yang Bertaqwa, Cinta Lingkungan serta Mampu Berkompetisi di Era Globalisasi<br>
<b>Misi :</b><br>
1.	Mewujudkan siswa yang religius, berkarakter, kreatif, inovatif, memiliki kecerdasan intelektual, emosional, spiritual, kinestetik dan religious serta cinta lingkungan.<br>
2.	Mewujudkan sekolah sehat yang bernuansa lingkungan serta lulusan yang  berwawasan  lingkungan.<br>
3.	Mewujudkan proses pembelajaran yang aktif, inovatif, kreatif, efektif dan menyenangkan dengan pemanfaatan lingkungan sekolah berbasis ICT<br>
4.	Mewujudkan tenaga pendidik dan kependidikan yang profesional, cakap,  berkarakter serta cinta lingkungan.<br>
5.	Mewujudkan standar sarana dan prasarana yang ramah lingkungan.<br>
6.	Menerapkan sistem pengelolaan manajemen yang bermutu terukur, akurat, komprehensif dan partisipatif dari semua pihak.<br>
7.	Menerapkan standar pembiayaan yang akuntabel dan transparan serta mengoptimalisasikan penggalian dana.<br>
8.	Mempertahankan Akreditasi Sekolah yang Amat Baik (A).<br>
<b>Tujuan :</b></br>
1.	Menciptakan siswa yang berkarakter unggul dan berbudaya lingkungan.<br>
2.	Menjadikan SMPN 8 Padang sebagai Sekolah Unggulan.<br>
3.	Memanfaatkan lingkungan sekolah dalam proses pembelajaran yang aktif, inovatif, kreatif, efektif dan menyenangkan.<br>
4.	Meningkatkan kompetensi dan kinerja pendidik dan tenaga kependidikan yang peduli dengan lingkungan.<br>
5.	Memiliki media pembelajaran yang lengkap, modern dan berbasis ICT dalam peningkatan kualitas pembelajaran.<br>
6.	Melaksanakan pelayanan yang prima, memuaskan, bertanggungjawab dan ramah lingkungan.<br>
7.	Memiliki manajemen sekolah berbasis ICT yang dapat diakses berbagai pihak.<br>
8.	Meningkatkan pelaksanaan K-10.<br>

