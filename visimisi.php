Visi dan Misi</h1>
<p>
<b>Visi :</b><br>
“mewujudkan insan yang bertaqwa,cerdas,mandiri dan kompetitif berbudaya lingkungan di era-globalisasi”<br>
<b>Misi :</b><br>
1. agama sebagai kebutuhan <br>
2.unggulan dalam prestasi akademik dan non-akademik <br>
3.mengembangkan kreatifitas dan inovasi dalam pencapaian SDM yang berkualitas <br>
4.menghasilkan lulusan yang IPTEK dan IMTAQ <br>
5.menghasilkan lulusan yang dapat bersaing ditengah masyarakat <br>
6.mewujudkan lulusan yang berkarakter peduli lingkungan <br>
7.mewujudkan lulusan yang memahami lingkungan yang bersih dan sehat. <br>
<br>
<b>Tujuan :</b></br>
<br>
1.menghasilkan kurikulum dan sistem pengujian berbasis kompetensi dengan pendidikan berbasis kecakapan hidup (lifeskill) serta peningkatan pelaksanaan sekolah rintisan SBI untuk menuju ke sekolah SBI <br>
2.menghasilkan peserta didik yang beriman dan bertaqwa kepada,tuhan yang maha esa dan berakhlak mulai. <br>
3.menghasilkan peserta didik agar menjadi manusia yang berkepribadian,cerdas,berkualitas dan berprestasi dalam bidang olaraga dan seni.<br>
4.menghasilkan peserta didik agar memiliki keterampilan teknologi informasi dan komunikasi serta mampu mengembangkan diri secara mandiri.<br>
5.menghasilkan peserta didik yang ulet dan gigih dalam berkompetisi,beradaptasi dengan lingkungan dan mengembangkan sikap sportifitas.<br>
6.menghasilkan peserta didik dengan ilmu pengetahuan dan teknologi agar mampu bersaing dan melanjutkan ke jenjang pendidikan yang lebih tinggi,dan terjun ke masyarakat.<br>
7.menghasilkan peserta didik yang bersih,sehat dan peduli lingkungan.<br>


</p>
